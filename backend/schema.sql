-- Daily Revenue View
CREATE MATERIALIZED VIEW daily_revenue AS
SELECT 
    DATE(paid_at) AS revenue_date,
    SUM(amount) AS total_revenue,
    COUNT(id) AS transaction_count
FROM payments
WHERE payment_status = 'Paid' AND deleted_at IS NULL
GROUP BY DATE(paid_at)
WITH DATA;

-- Monthly Revenue View
CREATE MATERIALIZED VIEW monthly_revenue AS
SELECT 
    DATE_TRUNC('month', paid_at) AS revenue_month,
    SUM(amount) AS total_revenue,
    COUNT(id) AS transaction_count
FROM payments
WHERE payment_status = 'Paid' AND deleted_at IS NULL
GROUP BY DATE_TRUNC('month', paid_at)
WITH DATA;

-- Doctor Statistics View
CREATE MATERIALIZED VIEW doctor_statistics AS
SELECT 
    d.id AS doctor_id,
    p.full_name AS doctor_name,
    dept.department_name,
    COUNT(v.id) AS total_visits,
    COUNT(CASE WHEN v.status = 'Closed' THEN 1 END) as completed_visits,
    COALESCE(SUM(inv.final_amount), 0) AS total_revenue_generated
FROM doctors d
JOIN profiles p ON d.user_id = p.id
LEFT JOIN departments dept ON d.department_id = dept.id
LEFT JOIN visits v ON d.id = v.doctor_id AND v.deleted_at IS NULL
LEFT JOIN invoices inv ON v.id = inv.visit_id AND inv.status = 'Paid' AND inv.deleted_at IS NULL
WHERE d.deleted_at IS NULL
GROUP BY d.id, p.full_name, dept.department_name
WITH DATA;

-- Create Indexes on Materialized Views for efficient lookup
CREATE UNIQUE INDEX idx_daily_revenue_date ON daily_revenue (revenue_date);
CREATE UNIQUE INDEX idx_monthly_revenue_month ON monthly_revenue (revenue_month);
CREATE UNIQUE INDEX idx_doctor_stats_id ON doctor_statistics (doctor_id);

