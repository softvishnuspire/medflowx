-- 2. SEQUENCES & AUTO-GENERATION FUNCTIONS
-- =========================================================================

CREATE SEQUENCE IF NOT EXISTS patient_code_seq START WITH 1;
CREATE SEQUENCE IF NOT EXISTS visit_number_seq START WITH 1;
CREATE SEQUENCE IF NOT EXISTS invoice_number_seq START WITH 1;

-- Patient Code Generator (Format: MED-YYYY-000001)
CREATE OR REPLACE FUNCTION generate_patient_code() 
RETURNS VARCHAR AS $$
BEGIN
  RETURN 'MED-' || TO_CHAR(CURRENT_DATE, 'YYYY') || '-' || LPAD(NEXTVAL('patient_code_seq')::TEXT, 6, '0');
END;
$$ LANGUAGE plpgsql;

-- Visit Number Generator (Format: VIS-YYYYMMDD-00001)
CREATE OR REPLACE FUNCTION generate_visit_number() 
RETURNS VARCHAR AS $$
BEGIN
  RETURN 'VIS-' || TO_CHAR(CURRENT_DATE, 'YYYYMMDD') || '-' || LPAD(NEXTVAL('visit_number_seq')::TEXT, 5, '0');
END;
$$ LANGUAGE plpgsql;

-- Invoice Number Generator (Format: INV-YYYYMMDD-00001)
CREATE OR REPLACE FUNCTION generate_invoice_number() 
RETURNS VARCHAR AS $$
BEGIN
  RETURN 'INV-' || TO_CHAR(CURRENT_DATE, 'YYYYMMDD') || '-' || LPAD(NEXTVAL('invoice_number_seq')::TEXT, 5, '0');
END;
$$ LANGUAGE plpgsql;

-- Automated updated_at trigger function
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

